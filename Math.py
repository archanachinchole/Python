# Addition
result_addition = 5 + 3
print("5 + 3 =", result_addition)

# Subtraction
result_subtraction = 7 - 2
print("7 - 2 =", result_subtraction)

# Multiplication
result_multiplication = 4 * 6
print("4 * 6 =", result_multiplication)

# Division
result_division = 15 / 3
print("15 / 3 =", result_division)

# Exponentiation
result_exponentiation = 2 ** 4
print("2 ** 4 =", result_exponentiation)

# Modulo (Remainder)
result_modulo = 17 % 4
print("17 % 4 =", result_modulo)
